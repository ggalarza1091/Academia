    private void txtPagoAdmiKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPagoAdmiKeyReleased
                int c=0;
        String texto=txtPagoAdmi.getText();
        if(texto.length()!=0){
            for(int i=0;i<texto.length();i++){
                if(texto.charAt(i)=='.'){
                    c++;
                }
            }
            if(c==2){
                txtPagoAdmi.setText(texto.substring(0,texto.length()-1));
            }
            if(c>2){
                for(int j=1;j<c;j++){
                    txtPagoAdmi.setText(texto.substring(0,texto.length()-1));
                    texto=txtPagoAdmi.getText();
                }
            }
            if(texto.charAt(0)=='.'){
                txtPagoAdmi.setText("0"+texto);
            }
        }
    }//GEN-LAST:event_txtPagoAdmiKeyReleased

